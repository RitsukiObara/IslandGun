namespace
{

}

//==========================
// コンストラクタ
//==========================
CBossStunState::CBossStunState()
{
	// 全ての値をクリアする
	m_nCount = 0;			// 経過カウント
}

//==========================
// デストラクタ
//==========================
CBossStunState::~CBossStunState()
{

}

//==========================
// 状態処理
//==========================
void CBossStunState::Process(CBoss* pBoss)
{
	// 経過カウントを加算する
	m_nCount++;
}

//==========================
// 情報の設定処理
//==========================
void CBossStunState::SetData(CBoss* pBoss)
{
	// 全ての値を設定する
	m_nCount = 0;			// 経過カウント

	// ダウンモーションを設定する
	pBoss->GetMotion()->Set(CBoss::MOTIONTYPE_DOWN);
}